     */
    
    /*
     |载入 app_config 文件
     |--------------------------------------------------------------------------
     */
    require '../app_config.php';
    
    /*
     |载入 SUBMAILAutoload 文件
     |--------------------------------------------------------------------------
     */
    
    require_once('../SUBMAILAutoload.php');
    
    /*
     |初始化 INTERNATIONALSMSXsend 类
     |--------------------------------------------------------------------------
     */
    
    $submail=new INTERNATIONALSMSXsend($intersms_configs);
    
    /*
     |必选参数
     |--------------------------------------------------------------------------
     |设置短信接收的国际手机号码，使用标准的 E164 格式，e.g. +1778889901，仅支持单个手机号码，不支持 +86 国内手机号码
     |--------------------------------------------------------------------------
     */
    
    $submail->setTo('+1778889901');
    
    /*
     |必选参数
     |--------------------------------------------------------------------------
     |设置短信模板ID
     |--------------------------------------------------------------------------
     */
    
    $submail->SetProject('xxxxx');
    
    /*
     |可选参数
     |--------------------------------------------------------------------------
     |添加文本变量
     |可多次调用
     |--------------------------------------------------------------------------
     */
    
    $submail->AddVar('code','198277');
    
    /*
     |调用 xsend 方法发送短信
     |--------------------------------------------------------------------------
     */
    
    $xsend=$submail->xsend();
    
    
    /*
     |打印服务器返回值
     |--------------------------------------------------------------------------
     */
    
    print_r($xsend);
